<?php
function suma($bs, $bs2)//FUNCION PARA LA SUMA Y DEMAS......
{
$resultado = $bs+$bs2;
return  $resultado;
}
function resta($bs, $bs2)
{
$resultado = $bs-$bs2;
return  $resultado;
}
function multiplicacion($bs, $bs2)
{
$resultado = $bs*$bs2;
return  $resultado;
}
function division($bs, $bs2)
{
$resultado = $bs/$bs2;
return  $resultado;
}
